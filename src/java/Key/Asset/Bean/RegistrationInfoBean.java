package Key.Asset.Bean;

/**
 *
 * @author home
 */
public class RegistrationInfoBean {
    
    private String reg_name;
    private String reg_contact;
    private String department;
    private String reg_date_of_joining;
    private String reg_email;
    private String reg_password;

    public String getReg_name() {
        return reg_name;
    }

    public void setReg_name(String reg_name) {
        this.reg_name = reg_name;
    }

    public String getReg_contact() {
        return reg_contact;
    }

    public void setReg_contact(String reg_contact) {
        this.reg_contact = reg_contact;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getReg_date_of_joining() {
        return reg_date_of_joining;
    }

    public void setReg_date_of_joining(String reg_date_of_joining) {
        this.reg_date_of_joining = reg_date_of_joining;
    }

    public String getReg_email() {
        return reg_email;
    }

    public void setReg_email(String reg_email) {
        this.reg_email = reg_email;
    }

    public String getReg_password() {
        return reg_password;
    }

    public void setReg_password(String reg_password) {
        this.reg_password = reg_password;
    }

    
    
    
}
