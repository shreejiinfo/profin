package Key.Asset.Bean;

/**
 *
 * @author home
 */
public class PrinterInfoBean {
    
    private String printer_brand;
    private String model_number;
    private String printer_price;
    private String purchase_date;
    private String vendor_name;
    private String printer_picture;

    public String getPrinter_brand() {
        return printer_brand;
    }

    public void setPrinter_brand(String printer_brand) {
        this.printer_brand = printer_brand;
    }

    public String getModel_number() {
        return model_number;
    }

    public void setModel_number(String model_number) {
        this.model_number = model_number;
    }

    public String getPrinter_price() {
        return printer_price;
    }

    public void setPrinter_price(String printer_price) {
        this.printer_price = printer_price;
    }

    public String getPurchase_date() {
        return purchase_date;
    }

    public void setPurchase_date(String Purchase_date) {
        this.purchase_date = Purchase_date;
    }

    public String getVendor_name() {
        return vendor_name;
    }

    public void setVendor_name(String Vendor_name) {
        this.vendor_name = Vendor_name;
    }

    public String getPrinter_picture() {
        return printer_picture;
    }

    public void setPrinter_picture(String printer_picture) {
        this.printer_picture = printer_picture;
    }
    
    
}
